package club.xianmusic.music.domain;

public class Banner {
	/** id */
	private int b_id;
	/** 标题 */
	private String b_title;
	/** 图片地址 */
	private String b_imgaddress;
	
	
	public int getB_id() {
		return b_id;
	}
	public void setB_id(int b_id) {
		this.b_id = b_id;
	}
	public String getB_title() {
		return b_title;
	}
	public void setB_title(String b_title) {
		this.b_title = b_title;
	}
	public String getB_imgaddress() {
		return b_imgaddress;
	}
	public void setB_imgaddress(String b_imgaddress) {
		this.b_imgaddress = b_imgaddress;
	}
	
	
	

}
